class PromedioMultimedial {
    private int id;
    private double promedio;

    public PromedioMultimedial(int id, double promedio) {
        this.id = id;
        this.promedio = promedio;
    }

    public int getId() {
        return this.id;
    }

    public double getPromedio() {
        return this.promedio;
    }
}
